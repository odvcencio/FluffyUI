// EnableEmoji enables emoji parsing in the default markup parser.
func EnableEmoji() {
	DefaultMarkupParser().EnableEmoji = true
}

// DisableEmoji disables emoji parsing in the default markup parser.
func DisableEmoji() {
	DefaultMarkupParser().EnableEmoji = false
}

// LookupEmoji returns the emoji for a name.
func LookupEmoji(name string) (string, bool) {
	value, ok := emojiMap[strings.ToLower(name)]
	return value, ok
}

// EmojiNames returns all known emoji names.
func EmojiNames() []string {
	names := make([]string, 0, len(emojiMap))
	for name := range emojiMap {
		names = append(names, name)
	}
	return names
}

// RegisterEmoji adds a custom emoji.
func RegisterEmoji(name, emoji string) {
	emojiMap[strings.ToLower(name)] = emoji
}

func replaceEmoji(text string) string {
	var out strings.Builder
	for i := 0; i < len(text); i++ {
		if text[i] != ':' {
			out.WriteByte(text[i])
			continue
		}
		start := i + 1
		end := start
		for end < len(text) && isEmojiNameChar(text[end]) {
			end++
		}
		if end > start && end < len(text) && text[end] == ':' {
			name := strings.ToLower(text[start:end])
			if value, ok := emojiMap[name]; ok {
				out.WriteString(value)
				i = end
				continue
			}
		}
		out.WriteByte(text[i])
	}
	return out.String()
}

func isEmojiNameChar(b byte) bool {
	return (b >= 'a' && b <= 'z') ||
		(b >= 'A' && b <= 'Z') ||
		(b >= '0' && b <= '9') ||
		b == '_' || b == '-' || b == '+'
}
